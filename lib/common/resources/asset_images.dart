extension ConvertToImage on String {
  Widget toSvg({
    BuildContext? context,
    double width = 24,
    double? height,
    double padding = 0,
    Color? color,
  }) {
    if (context != null) {
      return Padding(
        padding: EdgeInsets.all(padding),
        child: SvgPicture.asset(
          this,
          width: width,
          height: (height ?? width),
          color: color,
          cacheColorFilter: true,
        ),
      );
    }
    return Padding(
      padding: EdgeInsets.all(padding),
      child: SvgPicture.asset(
        this,
        width: width,
        height: height ?? width,
        color: color,
        cacheColorFilter: true,
      ),
    );
  }
}
